   function lookup_sc_field_0(&$sc_field_0) 
   {
      $conteudo = "" ; 
      if ($sc_field_0 == "in")
      { 
          $conteudo = "Entrada";
      } 
      if ($sc_field_0 == "out")
      { 
          $conteudo = "Saida";
      } 
      if (!empty($conteudo)) 
      { 
          $sc_field_0 = $conteudo; 
